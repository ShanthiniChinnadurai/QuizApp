package com.kg.convert.convert.model;

public class User{

public String name;
public String email;

    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }
     
    /**
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * @param email the email to set
     */
    public void setEmail(String email) {
        this.email = email;
    }

    /**
     * @return the email
     */
    public String getEmail() {
        return email;
    }
}
